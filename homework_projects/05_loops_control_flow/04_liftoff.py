# Function that prints countdown from 10 to 1 and then output Liftoff!
def main():

    # Loop through the numbers from 10 to 1 (inclusive) and print them.
    for n in range(10):

        # Print the number.
        print(10 - n)

    # Print the liftoff message.
    print("Liftoff!")


if __name__ == "__main__":
    main()